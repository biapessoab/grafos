public class Vertice {
    private int td, tt, father;

    Vertice() {
        td = 0;
        tt = 0;
        father = 0;
    }

    public int getTd() {
        return td;
    }
    public void setTd(int td) {
        this.td = td;
    }

    public int getTt() {
        return tt;
    }
    public void setTt(int tt) {
        this.tt = tt;
    }

    public int getFather() {
        return father;
    }
    public void setFather(int father) {
        this.father = father;
    }
}
